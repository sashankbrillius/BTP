// Extract YouTube video ID from URL
export function extractVideoId(url: string): string {
  const match = url.match(/(?:youtube\.com\/watch\?v=|youtu\.be\/)([^&\n?#]+)/);
  return match ? match[1] : '';
}

// Generate chapter structure for AIOps (11 chapters: 10 with 6 videos, 1 with 8 videos)
export function generateAIOpsChapters() {
  const chapters = [];
  let videoIndex = 0;

  for (let i = 1; i <= 11; i++) {
    const isLastChapter = i === 11;
    const videosInChapter = isLastChapter ? 8 : 6;
    
    chapters.push({
      domain: 'AIOps',
      chapterNumber: i,
      title: `AIOps Chapter ${i}`,
      description: `Comprehensive AIOps training - Chapter ${i}`,
      totalLessons: videosInChapter
    });

    // Generate lessons for this chapter
    for (let j = 1; j <= videosInChapter; j++) {
      if (videoIndex < AIOPS_VIDEOS.length) {
        const videoUrl = AIOPS_VIDEOS[videoIndex];
        const videoId = extractVideoId(videoUrl);
        // Will be handled by the lesson creation logic
        videoIndex++;
      }
    }
  }

  return chapters;
}

