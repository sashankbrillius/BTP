// Generate lessons for AIOps
export function generateAIOpsLessons(chapterId: number, chapterNumber: number) {
  const startIndex = (chapterNumber - 1) * 6;
  const isLastChapter = chapterNumber === 11;
  const videosInChapter = isLastChapter ? 8 : 6;
  const endIndex = startIndex + videosInChapter;
  
  const lessons = [];
  
  for (let i = 0; i < videosInChapter && (startIndex + i) < AIOPS_VIDEOS.length; i++) {
    const videoUrl = AIOPS_VIDEOS[startIndex + i];
    const videoId = extractVideoId(videoUrl);
    
    lessons.push({
      chapterId,
      domain: 'AIOps',
      lessonNumber: i + 1,
      title: `AIOps Lesson ${startIndex + i + 1}`,
      videoUrl,
      videoId,
      duration: '15:30',
      description: `Comprehensive AIOps training lesson ${startIndex + i + 1}`
    });
  }
  
  return lessons;
}

